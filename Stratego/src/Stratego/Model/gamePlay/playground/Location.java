package Stratego.Model.gamePlay.playground;

public class Location {

    Location nord;
    Location east;
    Location south;
    Location west;

    public Location getNord() {
        return nord;
    }

    public void setNord(Location nord) {
        this.nord = nord;
    }

    public Location getEast() {
        return east;
    }

    public void setEast(Location east) {
        this.east = east;
    }

    public Location getSouth() {
        return south;
    }

    public void setSouth(Location south) {
        this.south = south;
    }

    public Location getWest() {
        return west;
    }

    public void setWest(Location west) {
        this.west = west;
    }
}
